class Movie {
    constructor(id, title, posterUrl, year, cast, plot, ratings, language) {
        (this.id = id),
        (this.title = title),
        (this.posterUrl = posterUrl),
        (this.year = year),
        (this.cast = cast);
        (this.plot = plot),
        (this.ratings = ratings),
        (this.language = this.language);
    }
}

export default Movie;